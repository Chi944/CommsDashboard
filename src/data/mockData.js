// Each commodity has 30 days of price history (oldest -> newest)
const seeded = (seed) => {
  let s = seed;
  return () => {
    s = (s * 9301 + 49297) % 233280;
    return s / 233280;
  };
};

const buildHistory = (start, vol, seed) => {
  const rnd = seeded(seed);
  const arr = [];
  let p = start;
  for (let i = 29; i >= 0; i--) {
    p = p + (rnd() - 0.48) * vol;
    arr.push({
      day: `D-${i}`,
      date: new Date(Date.now() - i * 86400000).toISOString().slice(5, 10),
      price: Math.round(p * 100) / 100,
    });
  }
  return arr;
};

